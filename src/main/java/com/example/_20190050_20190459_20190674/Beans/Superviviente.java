package com.example._20190050_20190459_20190674.Beans;

public class Superviviente extends Humano{

    private float peso;
    private float fuerza;
    private String pareja;
    private float peso_cargado;

    public float getPeso() {
        return peso;
    }

    public void setPeso(float peso) {
        this.peso = peso;
    }

    public float getFuerza() {
        return fuerza;
    }

    public void setFuerza(float fuerza) {
        this.fuerza = fuerza;
    }

    public String getPareja() {
        return pareja;
    }

    public void setPareja(String pareja) {
        this.pareja = pareja;
    }

    public float getPeso_cargado() {
        return peso_cargado;
    }

    public void setPeso_cargado(float peso_cargado) {
        this.peso_cargado = peso_cargado;
    }
}
